# ==========================================
# Helper function to plot and save
# ==========================================

def make_plot(y, title, filename):
    plt.figure(figsize=(8,5))
    plt.plot(epochs, y)
    plt.xlabel("Epoch")
    plt.ylabel(title)
    plt.title(f"{title} Across Epochs")
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(filename, dpi=300)
    plt.close()

